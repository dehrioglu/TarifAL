import { SocialFeedPost, SocialRecipePost } from '../types';

export const recipePostToFeedPost = (post: SocialRecipePost): SocialFeedPost => ({
  id: post.id,
  type: 'recipe',
  authorId: post.authorId,
  recipeId: post.recipeId,
  title: post.title,
  text: post.description,
  imageUrl: post.imageUrl,
  tags: post.tags,
  createdAt: post.createdAt,
  likes: post.likes,
  commentsCount: post.commentsCount,
  saves: post.saves,
  shares: Math.max(18, Math.round(post.saves * 0.18)),
  prepTime: post.prepTime,
  cookTime: post.cookTime,
  totalTime: post.totalTime,
  difficulty: post.difficulty,
  servings: post.servings,
  marketBasketPrice: post.marketBasketPrice,
  restaurantOrderAvailable: post.restaurantOrderAvailable,
  restaurantStartPrice: post.restaurantStartPrice,
  commercialBadges: post.commercialBadges,
});


export const formatFollowerCount = (value: number) => {
  if (value >= 100000) {
    return `${Math.round(value / 1000)}B+`;
  }

  if (value >= 1000) {
    return `${(value / 1000).toFixed(1).replace('.', ',')}B`;
  }

  return String(value);
};

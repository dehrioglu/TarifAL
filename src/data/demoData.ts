import { GoalType, Ingredient, MarketProduct, Recipe, RecipeCategory, UserGoal } from '../types';

type IngredientSeed = [name: string, gram: number, price: number];

const slugify = (value: string) =>
  value
    .toLocaleLowerCase('tr-TR')
    .replace(/ğ/g, 'g')
    .replace(/ü/g, 'u')
    .replace(/ş/g, 's')
    .replace(/ı/g, 'i')
    .replace(/ö/g, 'o')
    .replace(/ç/g, 'c')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');

const ingredient = (recipeId: string, [name, gram, price]: IngredientSeed): Ingredient => ({
  id: `${recipeId}-${slugify(name)}`,
  name,
  gram,
  price,
});

const recipe = (params: {
  id: string;
  title: string;
  description: string;
  category: RecipeCategory;
  imageUrl: string;
  ingredients: IngredientSeed[];
  steps: string[];
  likes: number;
  prepTime: number;
  servings: number;
  calories: number;
  difficulty: Recipe['difficulty'];
  tags: string[];
  goalTypes: GoalType[];
  isFeatured?: boolean;
}): Recipe => {
  const ingredients = params.ingredients.map((item) => ingredient(params.id, item));

  return {
    id: params.id,
    title: params.title,
    description: params.description,
    category: params.category,
    imageUrl: params.imageUrl,
    ingredients,
    requiredIngredients: ingredients,
    optionalIngredients: [],
    steps: params.steps.map((text, index) => ({ id: `${params.id}-step-${index + 1}`, text })),
    likes: params.likes,
    prepTime: params.prepTime,
    servings: params.servings,
    calories: params.calories,
    difficulty: params.difficulty,
    tags: params.tags,
    goalTypes: params.goalTypes,
    estimatedPrice: ingredients.reduce((sum, item) => sum + item.price, 0),
    createdBy: 'demo-chef',
    createdByName: 'TarifAL Demo Şefi',
    createdAt: '2026-04-25T19:00:00.000Z',
    isFeatured: params.isFeatured,
  };
};

